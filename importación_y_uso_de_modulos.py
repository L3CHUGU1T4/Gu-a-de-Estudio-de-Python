import math # Importa el módulo math para funciones matemáticas avanzadas
from random import randint # Importa la función randint del módulo random para generar números aleatorios

pi = math.pi # Asigna el valor de pi del módulo math a la variable pi
randNum = randint(1,10) # Genera un número aleatorio entre el 1 y 10 a la variable randNum

print(f"El valor de π es: {pi}") 
print(f"El número aleatorio generado es: {randNum}")