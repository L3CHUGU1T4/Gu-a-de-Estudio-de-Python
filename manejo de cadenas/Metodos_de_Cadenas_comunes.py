text = "Hello World" #Asignamos una cadean de texto a la variable text

print(F"La cadena original es: {text}") #Mostramos al usuario la cadena existente
print(text.upper()) # Utilizamos upper para convertir la cadena en mayúsculas
print(text.capitalize()) # Capitaliza (Pone en mayúscula) la primer letra de la cadena de texto
print(text.replace("World", "Python")) #Remplaza la palabra World por python en la cadena de texto
